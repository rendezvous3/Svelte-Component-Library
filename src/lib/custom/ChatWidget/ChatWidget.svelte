<script lang="ts">
  import type { Snippet } from 'svelte';
  import WidgetIcon from '../WidgetIcon/WidgetIcon.svelte';
  import ChatWindow from '../ChatWindow/ChatWindow.svelte';
  import ChatInput from '../ChatInput/ChatInput.svelte';

  interface ChatWidgetProps {
    isOpen?: boolean;
    position?: 'bottom-right' | 'bottom-left' | 'top-right' | 'top-left';
    expanded?: boolean;
    darkMode?: boolean;
    subheader?: Snippet;
    onToggle?: () => void;
    onExpand?: (expanded: boolean) => void;
    onSend?: (message: string) => void;
    onClose?: () => void;
    children?: Snippet;
  }

  let {
    isOpen = false,
    position = 'bottom-right',
    expanded = false,
    darkMode = false,
    subheader,
    onToggle,
    onExpand,
    onSend,
    onClose,
    children
  }: ChatWidgetProps = $props();

  let isWidgetOpen = $state(false);
  let isExpanded = $state(expanded);

  // Sync with props
  $effect(() => {
    isWidgetOpen = isOpen;
  });

  $effect(() => {
    isExpanded = expanded;
  });

  let widgetClasses = $derived(
    [
      'chat-widget',
      `chat-widget--${position}`,
      isWidgetOpen && 'chat-widget--open',
      isExpanded && 'chat-widget--expanded',
      darkMode && 'chat-widget--dark'
    ]
      .filter(Boolean)
      .join(' ')
  );

  function toggleWidget() {
    isWidgetOpen = !isWidgetOpen;
    onToggle?.();
  }

  function handleExpand(expanded: boolean) {
    isExpanded = expanded;
    onExpand?.(expanded);
  }

  function handleClose() {
    // Small delay to allow animation
    setTimeout(() => {
      isWidgetOpen = false;
      onClose?.();
    }, 200);
  }

  function handleSend(message: string) {
    onSend?.(message);
  }
</script>

<div class={widgetClasses} data-theme={darkMode ? 'dark' : 'light'}>
  {#if isWidgetOpen}
    <div class="chat-widget__window">
      <div class="chat-widget__header">
        <div class="chat-widget__header-content">
          <WidgetIcon type="message-bubble" size="sm" color="#ffffff" />
          <span class="chat-widget__title">Chat Support</span>
        </div>
        <button
          class="chat-widget__close"
          onclick={handleClose}
          aria-label="Close chat"
          type="button"
        >
          <div class="chat-widget__close-icon">
            <svg
              width="20"
              height="20"
              viewBox="0 0 20 20"
              fill="none"
              xmlns="http://www.w3.org/2000/svg"
            >
              <path
                d="M15 5L5 15M5 5L15 15"
                stroke="currentColor"
                stroke-width="2"
                stroke-linecap="round"
                stroke-linejoin="round"
              />
            </svg>
          </div>
        </button>
      </div>
      
      <ChatWindow {expanded} onExpand={handleExpand} subheader={subheader} showScrollButton={true}>
        {#if children}
          {@render children()}
        {/if}
      </ChatWindow>

      <div class="chat-widget__input-wrapper">
        <ChatInput
          placeholder="Type a message..."
          onsend={handleSend}
        />
      </div>
    </div>
  {/if}
  
  <button
    class="chat-widget__button"
    onclick={toggleWidget}
    aria-label={isWidgetOpen ? 'Close chat' : 'Open chat'}
    aria-expanded={isWidgetOpen}
    type="button"
  >
    {#if isWidgetOpen}
      <svg
        width="24"
        height="24"
        viewBox="0 0 24 24"
        fill="none"
        xmlns="http://www.w3.org/2000/svg"
      >
        <path
          d="M18 6L6 18M6 6L18 18"
          stroke="currentColor"
          stroke-width="2"
          stroke-linecap="round"
          stroke-linejoin="round"
        />
      </svg>
    {:else}
      <WidgetIcon type="message-bubble" size="md" color="#ffffff" />
      <span class="chat-widget__badge">1</span>
    {/if}
  </button>
</div>

